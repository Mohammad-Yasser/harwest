template <class T>
istream& operator>>(istream& is, vector<T>& v) {
  for (auto& x : v) {
    is >> x;
  }
  return is;
}

template <class T>
ostream& operator<<(ostream& os, const vector<T>& v) {
  for (auto& x : v) {
    os << x << " ";
  }
  return os;
}

int n, m;
string s, t;
bool solve() {
  if (n > m + 1) return false;
  int wildcard = s.find('*');
  if (wildcard == string::npos) return s == t;
  string pre = s.substr(0, wildcard);
  string suf = s.substr(wildcard + 1);
  return pre == t.substr(0, sz(pre)) && suf == t.substr(sz(t) - sz(suf));
}

